function q(question: string, options: string[], answer: number) {
  return { question, options, answer };
}

// Helper to create a capsule with default empty arrays for new fields
function cap(data: {
  id: string; title: string; category: string; section: string;
  shortDescription: string; beginnerExplanation: string; detailedExplanation: string; simpleExplanation: string;
  facts: string[]; quiz: { question: string; options: string[]; answer: number }[];
  readTime: number; difficulty: Capsule["difficulty"]; icon: string;
  popular?: boolean; isNew?: boolean;
  introduction?: string; theory?: string; keyTerms?: KeyTerm[]; formulas?: string[];
  examples?: string[]; problemSolving?: ProblemSolution[]; images?: CapsuleImage[]; quickSummary?: string;
}): Capsule {
  return {
    introduction: data.introduction || data.beginnerExplanation,
    theory: data.theory || data.detailedExplanation,
    keyTerms: data.keyTerms || [],
    formulas: data.formulas || [],
    examples: data.examples || [],
    problemSolving: data.problemSolving || [],
    images: data.images || [],
    quickSummary: data.quickSummary || data.simpleExplanation,
    ...data,
  };
}

