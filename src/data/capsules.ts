export function getCapsulesByCategory(categoryId: string): Capsule[] {
  return capsules.filter(c => c.category === categoryId);
}

export function getCapsulesBySection(categoryId: string, sectionId: string): Capsule[] {
  return capsules.filter(c => c.category === categoryId && c.section === sectionId);
}

export function getPopularCapsules(): Capsule[] {
  return capsules.filter(c => c.popular);
}

export function getNewCapsules(): Capsule[] {
  return capsules.filter(c => c.isNew);
}

export function getRandomCapsule(): Capsule {
  return capsules[Math.floor(Math.random() * capsules.length)];
}

export function searchCapsules(query: string): Capsule[] {
  const q = query.toLowerCase();
  return capsules.filter(c =>
    c.title.toLowerCase().includes(q) ||
    c.shortDescription.toLowerCase().includes(q) ||
    c.category.toLowerCase().includes(q)
  );
}
